async function sharePdfWeb(html: string, filename: string, title: string): Promise<void> {
  const printWin = window.open('', '_blank');
  if (!printWin) {
    Alert.alert('Share failed', 'Please allow pop-ups to export the circular as PDF.');
    return;
  }
  printWin.document.write(html);
  printWin.document.title = filename.replace(/\.pdf$/i, '');
  printWin.document.close();
  printWin.focus();
  setTimeout(() => {
    printWin.print();
  }, 400);
  Alert.alert('Save as PDF', `"${title}" is ready. Choose "Save as PDF" in the print dialog. The file will use the circular title as its name.`);
}

async function sharePdfNative(html: string, filename: string, title: string): Promise<void> {
  const { uri } = await Print.printToFileAsync({ html });
  const dest = `${FileSystem.cacheDirectory}${filename}`;
  await FileSystem.copyAsync({ from: uri, to: dest });

  if (await Sharing.isAvailableAsync()) {
    await Sharing.shareAsync(dest, {
      mimeType: 'application/pdf',
      dialogTitle: title,
      UTI: 'com.adobe.pdf',
    });
    return;
  }

  Alert.alert('PDF ready', `Circular saved as ${filename}`);
}

export async function shareCircularAsPdf(input: CircularPdfInput): Promise<void> {
  const html = buildCircularHtml(input);
  const filename = sanitizeFilename(input.title);

  try {
    if (Platform.OS === 'web') {
      await sharePdfWeb(html, filename, input.title);
      return;
    }
    await sharePdfNative(html, filename, input.title);
  } catch {
    Alert.alert('Share failed', 'Could not generate the circular PDF. Please try again.');
  }
}

export function nextCircularNumber(existing: { circularNo: string }[]): string {
  const year = new Date().getFullYear();
  const prefix = `CIR/${year}/`;
  const max = existing.reduce((acc, item) => {
    const match = item.circularNo.match(/CIR\/\d{4}\/(\d+)/);
    const num = match ? parseInt(match[1], 10) : 0;
    return Math.max(acc, num);
  }, 0);
  return `${prefix}${String(max + 1).padStart(4, '0')}`;
}
