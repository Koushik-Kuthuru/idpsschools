#!/usr/bin/env node
/**
 * Generates supabase/migrations/*_seed_idps_departments_designations.sql
 * from scripts/lib/idps-departments-catalog.json
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

function slugify(value) {
  return String(value ?? "")
    .trim()
    .toUpperCase()
    .replace(/\s+/g, " ")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function sqlLiteral(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

const seed = JSON.parse(
  fs.readFileSync(path.join(__dirname, "lib/idps-departments-catalog.json"), "utf8")
);

const deptRows = seed.map((dept) => {
  const name = String(dept.name).trim().toUpperCase();
  const category = dept.category === "teaching" ? "teaching" : "non_teaching";
  return `  (${sqlLiteral(slugify(name))}, ${sqlLiteral(name)}, ${sqlLiteral(category)})`;
});

const desigBlocks = seed
  .map((dept) => {
    const deptSlug = slugify(dept.name);
    const rows = [...new Set(dept.designations.map((d) => String(d).trim().toUpperCase()))]
      .filter(Boolean)
      .map((name) => `    (${sqlLiteral(deptSlug)}, ${sqlLiteral(slugify(name))}, ${sqlLiteral(name)})`)
      .join(",\n");
    return rows;
  })
  .filter(Boolean);

const sql = `-- Seed IDPS departments and designations for all branches (idempotent)
-- Generated by scripts/generate-departments-seed-migration.mjs

WITH branch_targets AS (
  SELECT id AS branch_id
  FROM public.branches
  WHERE slug IN ('idpscherukupalli', 'idpskalaburagi')
),
dept_seed (slug, name, category) AS (
  VALUES
${deptRows.join(",\n")}
),
upserted_departments AS (
  INSERT INTO public.departments (branch_id, slug, name, category, status)
  SELECT b.branch_id, d.slug, d.name, d.category, 'Active'
  FROM branch_targets b
  CROSS JOIN dept_seed d
  ON CONFLICT (branch_id, slug) DO UPDATE SET
    name = EXCLUDED.name,
    category = EXCLUDED.category,
    status = 'Active',
    updated_at = NOW()
  RETURNING id, branch_id, slug
)
SELECT 1;

-- Designations (per department slug, all branches)
${seed
  .map((dept) => {
    const deptSlug = slugify(dept.name);
    const unique = [...new Set(dept.designations.map((d) => String(d).trim().toUpperCase()))].filter(
      Boolean
    );
    if (!unique.length) return "";
    const valueRows = unique
      .map((name) => `  (${sqlLiteral(deptSlug)}, ${sqlLiteral(slugify(name))}, ${sqlLiteral(name)})`)
      .join(",\n");
    return `WITH branch_targets AS (
  SELECT id AS branch_id
  FROM public.branches
  WHERE slug IN ('idpscherukupalli', 'idpskalaburagi')
),
desig_seed (dept_slug, slug, name) AS (
  VALUES
${valueRows}
)
INSERT INTO public.designations (branch_id, department_id, slug, name, status)
SELECT b.branch_id, d.id, s.slug, s.name, 'Active'
FROM branch_targets b
JOIN public.departments d ON d.branch_id = b.branch_id
JOIN desig_seed s ON s.dept_slug = d.slug
ON CONFLICT (department_id, slug) DO UPDATE SET
  name = EXCLUDED.name,
  status = 'Active',
  updated_at = NOW();`;
  })
  .filter(Boolean)
  .join("\n\n")}
`;

const outPath = path.join(
  root,
  "supabase/migrations/20260630120000_seed_idps_departments_designations.sql"
);
fs.writeFileSync(outPath, sql);
console.log(`Wrote ${outPath}`);
