export interface CircularPdfInput {
  title: string;
  body: string;
  circularNo: string;
  audience?: string;
  date?: string;
}

function sanitizeFilename(title: string): string {
  const base = title
    .trim()
    .replace(/[<>:"/\\|?*]/g, '')
    .replace(/\s+/g, '_')
    .slice(0, 80);
  return base.endsWith('.pdf') ? base : `${base || 'Circular'}.pdf`;
}

function escapeHtml(text: string): string {
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

